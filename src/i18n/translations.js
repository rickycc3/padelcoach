/** @typedef {'es' | 'en'} Locale */

function get(obj, path) {
  return path.split('.').reduce((o, key) => (o && typeof o === 'object' ? o[key] : undefined), obj)
}

/**
 * @param {Locale} locale
 * @param {string} path dot.path
 * @param {Record<string, string | number>} [vars] simple {{key}} replacement
 */
export function translate(locale, path, vars) {
  const dict = translations[locale] ?? translations.es
  let str = get(dict, path) ?? get(translations.es, path) ?? path
  if (vars && typeof str === 'string') {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replaceAll(`{{${k}}}`, String(v))
    }
  }
  return str
}

